package pl.mzebrows.shoots.ui;

/** Identifies which menu option is currently selected. */
public enum MenuEnum {

    /** Start a new game. */
    START_NEW_GAME,

    /** Continue the current game. */
    CONTINUE,

    /** Quit the game. */
    QUIT,

    /** Choose the number of players. */
    PLAYER_NUMBER_OPTION,

    /** Choose the number of rounds. */
    ROUND_NUMBER_OPTION,

    /** Choose the round duration. */
    ROUND_TIME_OPTION,

    /** No option selected. */
    NO_OPTION;
}
