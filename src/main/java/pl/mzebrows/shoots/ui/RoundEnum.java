package pl.mzebrows.shoots.ui;

/** The phases of a single round. */
public enum RoundEnum {

    /** The round is beginning. */
    ROUND_BEGIN,

    /** The round is in progress. */
    ROUND_CONTINUES,

    /** The round is ending. */
    ROUND_ENDS,

    /** The round is paused. */
    ROUND_PAUSED;
}
